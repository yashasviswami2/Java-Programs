package com.company;

public class cwh_22_ch4_dowhile
{
    public static void main(String[] args) {
        int a = 0;
        while (a<5){
            System.out.println(a);
            a++;
        }
        int b = 0;
        do {
            System.out.println(b);
            b++;
        }
        while (b<5);
    }
}
